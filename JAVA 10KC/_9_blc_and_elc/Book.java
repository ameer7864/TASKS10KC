package _9_blc_and_elc;
public class Book {
	int bookId;
	String title;
    String author;
	double price;
	public int getbookId() {
		return bookId;
	}
	public void setbookId(int bid) {
		bookId = bid;
	}
	public String gettitle() {
		return title;
	}
	public void settitle(String btitle) {
		title = btitle;
	}
    public String getauthor() {
		return author;
	}
	public void setauthor(String bauthor) {
		author = bauthor;
	}
	public double getprice() {
		return price;
	}
	public void setprice(double bal) {
	price = bal;
	}
}